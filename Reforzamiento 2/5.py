"""Ponemos el sueldo en una variable"""

variable_sueldo = 1547

"""Sacamos el módulo de la siguiente forma"""

par_impar = variable_sueldo % 2

"""Utilizamos condicional para verificar si el sueldo es par o impar"""

if par_impar == 0:
    print('El sueldo es un número par')
else:
    print('El sueldo es un número impar')